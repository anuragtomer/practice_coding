class Solution {
    void bfs(vector<vector<int>> &grid, int i, int j, queue<pair<int, int>> &bfsQueue, vector<vector<bool>> &visited) {
        visited[i][j] = true;
        bfsQueue.push({i, j});
        vector<vector<int>> direction = {{0, 1}, {0, -1}, {1, 0}, {-1, 0}};
        for (auto dir : direction) {
            int x = i + dir[0], y = j + dir[1];
            if (x < 0 || y < 0 || x >= grid.size() || y >= grid[x].size() || visited[x][y] || grid[x][y] != 1)
                continue;
            bfs(grid, x, y, bfsQueue, visited);
        }
    }

   public:
    int shortestBridge(vector<vector<int>> &grid) {
        int m = grid.size(), n = grid[0].size();
        vector<vector<bool>> visited(m, vector<bool>(n, false));
        auto print = [&]() {
            for (auto row : visited) {
                for (auto val : row) {
                    cout << val << " ";
                }
                cout << endl;
            }
        };
        bool done = false;
        queue<pair<int, int>> q;
        for (int i = 0; i < m && !done; ++i) {
            for (int j = 0; j < n && !done; ++j) {
                if (grid[i][j] == 1) {
                    bfs(grid, i, j, q, visited);
                    done = true;
                }
            }
        }
        int minBridgeLen = 0;
        vector<vector<int>> direction = {{0, 1}, {0, -1}, {1, 0}, {-1, 0}};

        while (!q.empty()) {
            int len = q.size();
            minBridgeLen++;
            while (len--) {
                auto top = q.front();
                q.pop();
                int i = top.first, j = top.second;
                for (auto dir : direction) {
                    int x = i + dir[0], y = j + dir[1];
                    if (x < 0 || y < 0 || x >= grid.size() || y >= grid[x].size() || visited[x][y])
                        continue;
                    if (grid[x][y] == 1)
                        return minBridgeLen - 1;
                    visited[x][y] = true;
                    q.push({x, y});
                }
            }
        }
        return -1;
    }
};

