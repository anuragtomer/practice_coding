/**
 * // This is the BinaryMatrix's API interface.
 * // You should not implement it, or speculate about its implementation
 * class BinaryMatrix {
 *   public:
 *     int get(int x, int y);
 *     vector<int> dimensions();
 * };
 */
class BinaryMatrix {
    vector<vector<int>> matrix;
    int x = 0, y = 0;
    int counter = 0;

   public:
    BinaryMatrix(vector<vector<int>> _matrix) {
        matrix = _matrix;
        x = matrix.size();
        if (x > 0)
            y = matrix[0].size();
    }
    int get(int _x, int _y) {
        // assert(counter <= 1000);
        assert(_x < x || _y < y);
        counter++;
        return matrix[_x][_y];
    }
    vector<int> dimensions() {
        return {x, y};
    }
    int getCounter() {
        return counter;
    }
};

class Solution {
   public:
    /*
        More accesses: Kind of linear.
     int leftMostColumnWithOne(BinaryMatrix &binaryMatrix) {
         vector<int> dim = binaryMatrix.dimensions();
         int rows = dim[0];
         int cols = dim[1];
         int candidate = -1;
         for (int r = 0, c = cols - 1; r < rows && c >= 0;)
             if (binaryMatrix.get(r, c) == 1) {
                 candidate = c;
                 c--;
             } else
                 r++;
         return candidate;
     }*/
    int leftMostColumnWithOne(BinaryMatrix &binaryMatrix) {
        vector<int> dim = binaryMatrix.dimensions();
        int R = dim[0], C = dim[1];
        int minC = C;
        int beg = 0, end = C - 1;
        vector<int> rows;
        // Do binary search over each row, and keep reducing binary search range if you find '1'.
        for (int i = 0; i < R; i++)
            rows.push_back(i);
        std::random_device rd;
        std::mt19937 g(rd());

        shuffle(rows.begin(), rows.end(), g);
        for (auto r : rows) {
            beg = 0, end = minC - 1;
            if (binaryMatrix.get(r, end) == 0)
                continue;
            while (beg <= end) {
                int mid = beg + (end - beg) / 2;
                if (binaryMatrix.get(r, mid) == 1) {
                    minC = min(minC, mid);
                    end = mid - 1;
                } else
                    beg = mid + 1;
            }
        }
        return minC == C ? -1 : minC;
    }
};
